package com.askey.model;

public class Tpickedupdetail {
	private String item_name;
	private String datecode;
	private String pickup_qty;
	private String pick_times;
	private String create_time;
	//
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getDatecode() {
		return datecode;
	}
	public void setDatecode(String datecode) {
		this.datecode = datecode;
	}
	public String getPickup_qty() {
		return pickup_qty;
	}
	public void setPickup_qty(String pickup_qty) {
		this.pickup_qty = pickup_qty;
	}
	public String getPick_times() {
		return pick_times;
	}
	public void setPick_times(String pick_times) {
		this.pick_times = pick_times;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
}
